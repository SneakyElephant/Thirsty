import Foundation

extension UIColor {
    class func backgroundColor () -> UIColor {
        return UIColor(red: 162, green: 68, blue: 200, alpha: 1)
    }
    
    class func buttonColor () -> UIColor {
        return UIColor(red: 176, green: 91, blue: 219, alpha: 1)
    }
    
    class func listItemsColor () -> UIColor {
        return UIColor(red: 186, green: 103, blue: 228, alpha: 1)
    }
    
    class func selectedColor () -> UIColor {
        return UIColor(red: 109, green: 37, blue: 138, alpha: 1)
    }
}